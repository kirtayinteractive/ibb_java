        short primitiveTypeShort=32767;
        int primitiveTypeInt=2147483647;
        long primitiveTypeLong=9222222222222L;

        float primitiveTypeFloat=14.23f;
        double primitiveTypeDouble=14.23;

        boolean primitiveTypeBoolean=true;
